package biz.entreprise;

public interface EntrepriseDto {

  public String toString();

  public int getIdEntreprise();

  public void setIdEntreprise(int idEntreprise);

  public String getDenomination();

  public void setDenomination(String denomination);

  public String getAdresse();

  public void setAdresse(String adresse);

  public String getNumero();

  public void setNumero(String numero);

  public String getBoite();

  public void setBoite(String boite);

  public String getCodePostal();

  public void setCodePostal(String codePostal);

  public String getVille();

  public void setVille(String ville);

  public String getEmail();

  public void setEmail(String email);

  public String getTel();

  public void setTel(String tel);

  public boolean getEstBlackListe();

  public void setEstBlackListe(boolean estBlackListe);

  public int getNumVersion();

  public void setNumVersion(int numVersion);

  public boolean getEstSupprime();

  public void setEstSupprime(boolean estSupprime);

  /**
   * rempli l'entreprise avec les différents paramètres.
   * 
   * @param idEntreprise le numéro de l'entreprise
   * @param denomination une chaîne de caractère représentant le nom de l'entreprise
   * @param adresse une chaîne de caractère représentant la rue de l'entreprise
   * @param numero une chaîne de caractère représentant le numéro de la rue de l'entreprise
   * @param boite une chaîne de caractère représentant la boîte postale de l'entreprise
   * @param codePostal une chaîne de caractère représentantcodePostal le code postal de l'entreprise
   * @param ville une chaîne de caractère représentant ville la ville de l'entreprise
   * @param email une chaîne de caractère représentant email l'email de l'entreprise
   * @param tel une chaîne de caractère représentant tel le telephone de l'entreprise
   * @param estBlackListe un booléen si true l'entreprise est blacklistée, faux sinon
   * @param numVersion le numéro de version de l'entreprise en DB
   * @param estSupprime un boolean indiquant si l'entreprise est "supprimée" ou non
   */
  public void fillEntreprise(int idEntreprise, String denomination, String adresse, String numero,
      String boite, String codePostal, String ville, String email, String tel,
      boolean estBlackListe, int numVersion, boolean estSupprime);

}
